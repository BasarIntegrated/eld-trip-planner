export const VALID_US_STATE_CODES = new Set(Object.values(US_STATE_CODES));

export function isValidUsStateCode(code: string): boolean {
  return VALID_US_STATE_CODES.has(code.toUpperCase());
}

export function stateNameToCode(name: string | undefined): string | null {
  if (!name) return null;
  return US_STATE_CODES[name] ?? null;
}
